// Operadores aritméticos

// multiplicação *
console.log(12 * 2)

// divisão /
console.log(10/2)

// soma +
console.log(12 + 5)

// subtracao -
console.log(10 - 9)

// resto da divisão %
console.log( 10 % 4)

