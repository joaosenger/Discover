/*
    Palavras reservadas para criar uma variáveis:
        var
        let
        const
*/

var clima = "Quente"
console.log(clima)

clima = "Frio"
console.log(clima)

let saldo = "Positivo"
console.log(saldo)

saldo = "Negativo"
console.log(saldo)

// constante não pode mudar!
const cpf = 12345678910;
console.log(cpf)

// vai dar erro, pois o valor das constantes não pode mudar!
cpf = 1234;
console.log(cpf)